    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int row= tableHP.getSelectedRow();
        try {
            hocPhiModel= hocPhiService.getHocPhiByMa(tableHP.getValueAt(row, 0).toString());
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
       String ngthu =dateFormat.format(txtNgaythu.getDate());
      hocPhiModel.setNgayThu(ngthu);
      hocPhiModel.setTienHP(Double.parseDouble(txtHP.getText()));
      hocPhiModel.setTienDaDong(Double.parseDouble(txtHPDD.getText()));
      hocPhiModel.setMaTre(cbMatre.getSelectedItem().toString());
      hocPhiModel.setManv(cbNV.getSelectedItem().toString());
      if(rbChuatt.isSelected())
          hocPhiModel.setTtrang("Chưa thanh toán");
      if(rbDatt.isSelected())
          hocPhiModel.setTtrang("Đã thanh toán");
      hocPhiService.updateHocPhi(hocPhiModel);
      deTableModel.setRowCount(0);
         List<HocPhiModel> hPhi= hocPhiService.getHocPHi();
        for(HocPhiModel hocPhi : hPhi){
            deTableModel.addRow(new Object[]{hocPhi.getMaHP(), hocPhi.getNgayThu(), hocPhi.getTienHP(), hocPhi.getTienDaDong(), hocPhi.getTtrang(), hocPhi.getMaTre(), hocPhi.getManv()});
        }
        } catch (IOException ex) {
            Logger.getLogger(HocPhiPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(HocPhiPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_editButtonActionPerformed

    private void tableHPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableHPMouseClicked
        // TODO add your handling code here:
        int row = tableHP.getSelectedRow();
        String mahp= tableHP.getValueAt(row, 0).toString();
        Date date;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) tableHP.getValueAt(row, 1).toString());
            txtNgaythu.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(HocPhiPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
            String tienhp= tableHP.getValueAt(row, 2).toString();
            String tienda= tableHP.getValueAt(row, 3).toString();
            String matre=   tableHP.getValueAt(row, 5).toString();
            String manv= tableHP.getValueAt(row, 6).toString();
            String tinhtrang= tableHP.getValueAt(row, 4).toString();
            if(tinhtrang.equals("Chưa thanh toán"))
            {
                rbChuatt.setSelected(true);
            rbDatt.setSelected(false);
            }
            if(tinhtrang.equals("Đã thanh toán"))
            {
                rbDatt.setSelected(true);
                rbChuatt.setSelected(false);
            }
            txtMaHP.setText(mahp);
            txtHP.setText(tienhp);
            txtHPDD.setText(tienda);
            cbMatre.setSelectedItem(matre);
            cbNV.setSelectedItem(manv);
    }//GEN-LAST:event_tableHPMouseClicked

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
         int row = tableHP.getSelectedRow();
       if(row == -1)
       {
           JOptionPane.showMessageDialog(HocPhiPanel.this,"Vui lòng chọn biên lai để xóa", "Lỗi",JOptionPane.ERROR_MESSAGE);
       }
       else{
           int confirm = JOptionPane.showConfirmDialog(HocPhiPanel.this,"Bạn có chắc muốn xóa không ?");
           if (confirm == JOptionPane.YES_OPTION)
           {
               String mahp = (String) tableHP.getValueAt(row, 0);
               hocPhiService.deleteHocPhi(mahp);
               deTableModel.setRowCount(0);
            List<HocPhiModel> hPhi= hocPhiService.getHocPHi();
        for(HocPhiModel hocPhi : hPhi){
            deTableModel.addRow(new Object[]{hocPhi.getMaHP(), hocPhi.getNgayThu(), hocPhi.getTienHP(), hocPhi.getTienDaDong(), hocPhi.getTtrang(), hocPhi.getMaTre(), hocPhi.getManv()});
        }
           }
       }
    }//GEN-LAST:event_deleteButtonActionPerformed


