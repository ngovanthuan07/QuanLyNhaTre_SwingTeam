    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if(txtHP.getText().isEmpty()||txtHPDD.getText().isEmpty())
            JOptionPane.showMessageDialog(printButton, "Chưa nhập đủ thông tin");
        else{
              hocPhiModel.setMaHP("");
        hocPhiModel.setMaTre(cbMatre.getSelectedItem().toString());
        hocPhiModel.setTienHP(Double.parseDouble(txtHP.getText()));
        if(rbChuatt.isSelected())
        hocPhiModel.setTtrang("Chưa thanh toán");
        else
        if(rbDatt.isSelected())
        hocPhiModel.setTtrang("Đã thanh toán");
        hocPhiModel.setTienDaDong(Double.parseDouble(txtHPDD.getText()));
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String ngthu =dateFormat.format(txtNgaythu.getDate());
        hocPhiModel.setNgayThu(ngthu);
        hocPhiModel.setManv(cbNV.getSelectedItem().toString());
        hocPhiService.addHocPHi(hocPhiModel);
        deTableModel.setRowCount(0);
         List<HocPhiModel> hPhi= hocPhiService.getHocPHi();
        for(HocPhiModel hocPhi : hPhi){
            deTableModel.addRow(new Object[]{hocPhi.getMaHP(), hocPhi.getNgayThu(), hocPhi.getTienHP(), hocPhi.getTienDaDong(), hocPhi.getTtrang(), hocPhi.getMaTre(), hocPhi.getManv()});
        }
        }
      
    }//GEN-LAST:event_addButtonActionPerformed

